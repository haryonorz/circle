class Location {
  String region;
  String subRegion;
  String country;
  String location;
  String address;
  double latitude;
  double longitude;

  Location({
    required this.region,
    required this.subRegion,
    required this.country,
    required this.location,
    required this.address,
    required this.latitude,
    required this.longitude,
  });
}
